#include <stdio.h>

#include "zend.h"

#include <stdarg.h>

#if ZEND_BROKEN_SPRINTF
int zend_sprintf(char *buffer, const char *format, ...)
{
	int len;
	va_list args;

	va_start(args, format);
	len = vsprintf(buffer, format, args);
	va_end(args);

	return len;
}
#endif

